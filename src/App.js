export default function App() {
  const [items, setItems] = useState([]);

  function handleAddItems(newItemObj) {
    setItems((currentItems) => [...currentItems, newItemObj]);
  }

  return (
    <div className="app">
      <Logo />
      <Form onAddItems={handleAddItems} />
      <PackingList items={items} />
      <Stats />
    </div>
  );
}

